package ru.binarysimple.geekhab;

public class User {
    private String login;
    private String id;
    private String score;
    private String avatar_url;
    private String url;

    public String getLogin() {
        return login;
    }

    public String getId() {
        return id;
    }

    public String getScore() {
        return score;
    }

    public String getAvatar_url() {
        return avatar_url;
    }

    public String getUrl() {
        return url;
    }
}

